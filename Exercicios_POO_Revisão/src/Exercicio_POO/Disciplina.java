package Exercicio_POO;

public class Disciplina {
    private int codigoDiscip; 
    private String nomeDisciplina; 
    private float cargaHoraGeral; 

    public int getCodigoDiscip() {
        return codigoDiscip;
    }

    public void setCodigoDiscip(int codigoDiscip) {
        this.codigoDiscip = codigoDiscip;
    }

    public String getNomeDisciplina() {
        return nomeDisciplina;
    }

    public void setNomeDisciplina(String nomeDisciplina) {
        this.nomeDisciplina = nomeDisciplina;
    }

    public float getCargaHoraGeral() {
        return cargaHoraGeral;
    }

    public void setCargaHoraGeral(float cargaHoraGeral) {
        this.cargaHoraGeral = cargaHoraGeral;
    }

    
}